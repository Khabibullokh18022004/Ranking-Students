import Foundation



func getScores(from input: String) -> [String: Double] {
    var scores = [String: Double]()
    let lines = input.split(separator: "\n")

    for line in lines {
        let items = line.split(separator: " ")
        if items.count == 2 {
            let id = String(items[0])
            let scoreStr = String(items[1]).replacingOccurrences(of: ",", with: ".")
            if let score = Double(scoreStr) {
                scores[id] = score
            }
        }
    }

    return scores
}
